from __future__ import annotations

import textwrap
from pathlib import Path

import yaml

from agentroles.models import AgentRolesConfig, TargetType
from agentroles.plugin import GenerationResult, TargetGenerator
from agentroles.validators import extract_model_family, is_claude_model

ANTHROPIC_MARKER = "anthropic/"


class ClaudeCodeGenerator(TargetGenerator):
    @property
    def target_type(self) -> TargetType:
        return TargetType.CLAUDE_CODE

    def _build_frontmatter_comment(self, role_name: str, primary: str) -> str:
        return (
            f"# WARNING: Primary model '{primary}' is not an Anthropic Claude model.\n"
            f"# Claude Code's model: field only supports sonnet, opus, haiku, or inherit.\n"
            f"# model: inherit has been set as a fallback. Use this agent file as-is if you\n"
            f"# route requests through the generated LiteLLM proxy config\n"
            f"# (litellm-config.yaml). Otherwise, replace the model field with an Anthropic\n"
            f"# model name (sonnet, opus, haiku) for direct Claude Code use.\n"
        )

    def _compute_model_field(self, role_name: str, primary: str) -> tuple[str, str]:
        """Return (model_value, frontmatter_comment)."""
        if is_claude_model(primary):
            family = extract_model_family(primary)
            return family, ""
        return "inherit", self._build_frontmatter_comment(role_name, primary)

    def _build_agent_markdown(self, role_name: str, role_config, config: AgentRolesConfig) -> str:
        model_value, comment = self._compute_model_field(role_name, role_config.primary)

        tools = self._resolve_tools(config)

        notes = role_config.notes or f"{role_name} agent — generated by agentroles"
        description = notes[:200]

        frontmatter = {
            "name": role_name,
            "description": description,
            "model": model_value,
            "tools": tools,
        }

        lines: list[str] = ["---"]
        for key, value in frontmatter.items():
            if isinstance(value, list):
                lines.append(f"{key}: [{', '.join(value)}]")
            else:
                lines.append(f"{key}: {value}")
        lines.append("---")

        if comment:
            lines.append("")
            lines.append(comment.rstrip())

        lines.append("")
        lines.append(f"# {role_name.title()} Agent")
        lines.append(f"# Generated by agentroles — source of truth: agentroles.yaml")
        lines.append("")
        lines.append(f"You are the **{role_name}** agent. {notes}")

        return "\n".join(lines) + "\n"

    def _resolve_tools(self, config: AgentRolesConfig) -> list[str]:
        return ["*"]

    def generate(self, config: AgentRolesConfig, base_dir: Path, result: GenerationResult) -> None:
        output_dir = config.get_target_path(self.target_type)
        if not output_dir:
            return

        target_dir = base_dir / output_dir
        target_dir.mkdir(parents=True, exist_ok=True)

        for role_name, role_config in config.roles.items():
            markdown = self._build_agent_markdown(role_name, role_config, config)
            file_path = target_dir / f"{role_name}.md"
            file_path.write_text(markdown)
            result.add_file(str(file_path))
